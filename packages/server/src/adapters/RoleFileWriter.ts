/**
 * Cross-CLI Role File Writers.
 *
 * Each CLI has a different format for agent definition files.
 * RoleFileWriter translates Flightdeck role definitions into
 * CLI-specific files so each CLI can discover and use custom roles.
 *
 * See docs/research/custom-agents-cross-cli.md for format details.
 */
import { mkdir, writeFile, readdir, unlink, readFile } from 'fs/promises';
import { join, resolve, sep } from 'path';

// ── Types ───────────────────────────────────────────────────────────

export interface RoleDefinition {
  /** Role identifier, e.g. 'developer', 'architect' */
  role: string;
  /** Short description of the role */
  description: string;
  /** Full system prompt / instructions for the agent */
  instructions: string;
  /** Optional list of tool names the agent can use */
  tools?: string[];
}

export interface RoleFileWriter {
  /** Write CLI-specific agent files for the given roles. Returns paths of written files. */
  writeRoleFiles(roles: RoleDefinition[], targetDir: string): Promise<string[]>;
  /** Remove previously generated Flightdeck agent files. */
  cleanRoleFiles(targetDir: string): Promise<void>;
  /** Return a human-readable format identifier. */
  getFormat(): string;
}

// ── Constants ───────────────────────────────────────────────────────

/** Marker comment embedded in generated markdown files for identification. */
export const FLIGHTDECK_MARKER = '<!-- Generated by Flightdeck. Do not edit manually. -->';

const FLIGHTDECK_JSON_KEY = '_generatedByFlightdeck';

/** Only lowercase alphanumeric characters and hyphens are allowed in role names. */
const VALID_ROLE_NAME = /^[a-z0-9][a-z0-9-]*$/;

// ── Copilot: .github/agents/<role>.agent.md ─────────────────────────

export class CopilotRoleFileWriter implements RoleFileWriter {
  getFormat(): string {
    return 'copilot-agent-md';
  }

  async writeRoleFiles(roles: RoleDefinition[], targetDir: string): Promise<string[]> {
    const dir = join(targetDir, '.github', 'agents');
    await mkdir(dir, { recursive: true });

    const writtenFiles: string[] = [];
    for (const role of roles) {
      validateRoleName(role.role);
      const filename = `flightdeck-${role.role}.agent.md`;
      const filePath = join(dir, filename);
      assertPathWithinDir(filePath, dir);
      const toolsList = (role.tools?.length ? role.tools : ['read', 'edit', 'search', 'shell'])
        .map((t) => `  - ${t}`)
        .join('\n');

      const content = [
        FLIGHTDECK_MARKER,
        '---',
        `name: flightdeck-${role.role}`,
        `description: "${escapeYamlString(role.description)}"`,
        'tools:',
        toolsList,
        '---',
        '',
        `# ${capitalize(role.role)} — Flightdeck Agent`,
        '',
        role.instructions,
        '',
      ].join('\n');

      await writeFile(filePath, content, 'utf-8');
      writtenFiles.push(filePath);
    }
    return writtenFiles;
  }

  async cleanRoleFiles(targetDir: string): Promise<void> {
    const dir = join(targetDir, '.github', 'agents');
    await removeMatchingFlightdeckFiles(dir, 'flightdeck-', '.agent.md');
  }
}

